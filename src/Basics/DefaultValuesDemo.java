package basics;

public class DefaultValuesDemo {

    static int num;
    static boolean flag;
    static double value;
    static String text;

    public static void main(String[] args) {
        System.out.println("Default int: " + num);
        System.out.println("Default boolean: " + flag);
        System.out.println("Default double: " + value);
        System.out.println("Default String: " + text);
    }
}
